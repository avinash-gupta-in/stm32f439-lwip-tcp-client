/**
  * @brief  Function implementing the defaultTask thread.
  * @param  argument: Not used
  * @retval None
  */
/* USER CODE END Header_StartDefaultTask */
void StartDefaultTask(void *argument)
{
  /* init code for LWIP */
  MX_LWIP_Init();

  /* USER CODE BEGIN 5 */
  printf("Simple TCP Client example for Nucleo 439 using lwIP stack\r\n ");
  printf("---------------------------------------------------------\r\n ");
  osDelay(2000);

  while(is_net_up==0)
  {
	  printf("Waiting for Ethernet Cable plug in ... \r\n ");
	  osDelay(1000);
  }
  //Simple TCP Client!
  //IP address of the remote server
  ip_addr_t server_ip;

  //This holds the return values from network functions
  //mainly used to check error or success
  err_t status;

  //This struct represent a network connection
  struct netconn *conn;

  //Allocate resources for a new connection
  //Parameter tells what type of connection we need
  //Other option is for NETCONN_UDP
  conn=netconn_new(NETCONN_TCP);

  IP4_ADDR(&server_ip,192,168,1,22);//My PC's local IP address is 192.168.2.17

  printf("Connecting to server @ %s... (edit line 322 in main.c)\r\n ",ip4addr_ntoa(&server_ip));

  status=netconn_connect(conn, &server_ip, 5000);

  if(status==ERR_OK)
  {
      printf("Connected to server! \r\n ");
  }
  else
  {
      printf("Sorry ! Could not connect to server.\r\n ");

      while(1);
  }

  //Send a Hello Message 5 time, waiting 1 second
  for(int i=0;i<5;i++)
  {
      printf("Sending data to server \r\n ");

      status=netconn_write(conn,"HELLO FROM STM32\r\n",18,NETCONN_NOCOPY);

      if(status!=ERR_OK)
      {
          //Sending failed
          printf("Sending failed! \r\n ");

          netconn_delete(conn);

          while(1);
      }
      else if(status==ERR_OK)
      {
          printf("Data sent successfully ! \r\n\r\n ");
      }

      osDelay(1000);

  }//end for loop, 5 times looping done

  //Clean up resources
  netconn_close(conn);
  netconn_delete(conn);

  printf("Application ended ! \r\n ");

  while(1);//thread function should not return
  /* USER CODE END 5 */
}

